package com.cobblemon.mod.common.client.render.block

import com.cobblemon.mod.common.block.entity.PokeCakeBlockEntity
import com.mojang.blaze3d.vertex.PoseStack
import net.minecraft.client.renderer.MultiBufferSource
import net.minecraft.client.renderer.blockentity.BlockEntityRenderer
import net.minecraft.client.renderer.blockentity.BlockEntityRendererProvider

class PokeCakeBlockEntityRenderer(ctx: BlockEntityRendererProvider.Context) : BlockEntityRenderer<PokeCakeBlockEntity> {

    override fun render(
        blockEntity: PokeCakeBlockEntity,
        partialTick: Float,
        poseStack: PoseStack,
        bufferSource: MultiBufferSource,
        packedLight: Int,
        packedOverlay: Int
    ) {
        val cookingComponent = blockEntity.cookingComponent
        renderCake(cookingComponent, poseStack, bufferSource, packedLight, blockEntity.bites)
    }
}